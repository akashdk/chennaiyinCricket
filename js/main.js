$(document).ready(function(){
    var scrollTop = 0; 
    $(window).scroll(function(){
        var scrollTop = $(window).scrollTop();
        if(scrollTop >= 50){
            $('.full-logo').addClass('small');
            $('.nav').addClass('new');
        }else {
            $('.full-logo').removeClass('small');
            $('nav').removeClass('new');
        }
    });
    // Tamil Translation
    $('.lang').change(function(){
		var lang = $(this).val();
		$('.tr').each(function(i){
			$(this).text(aLangKeys[lang][$(this).attr('key')]);
		});
    });
    // age calc
    $('#dob').change(function(){
        var birthDate = $('#dob').val();
        var newage =  calculateage(birthDate);
        if(newage < 18){
            alert('Sorry You must be 18 years older');
            $('.age').html("Sorry You must be 18 years older");
        }else{
            $('.age').html("You are"+" "+ newage + " " + "years old");
        }
    });

    var calculateage = function(birthDate){
        var currentyear = new Date().getFullYear();
        var birthYear = new Date(birthDate).getFullYear();
        var age = currentyear - birthYear;

        return age;
    }
    
});